<h3> Histoire </h3>
<p>Créé en 1986, le club atteint la Nationale 2 sept ans plus tard. Pour sa première année à ce niveau, le club fini dans les barragistes pour l'ascension en Nationale 1. L'année suivante, ils terminent vice-champion de France de N2 et accèdent pour la première fois de la jeune histoire du club en Nationale 1. Ils se maintiennent dès leur première année et en terminant à la cinquième place en 1997, le club décroche sa première qualification pour la Coupe d'Europe mais est éliminé de la compétition dès le premier tour par Tomar (Portugal)1. En 1999, le club est relégué et 
ne reste qu'une année en Nationale 2, terminant deuxième de sa poule et
retournant dans l'élite. Malheureusement, le club reprend l'ascenseur et
retourne en Nationale 1 qu'en 2003 grâce au titre de Champion de France de Nationale 2. Deux ans plus tard, le club réussit une belle saison en retrouvant la cinquième place et la Coupe d'Europe, stade déjà atteint il y a huit ans et en perdant la seule finale de Coupe de France disputé à ce jour par les Bretons. La saison suivante, les hommes réitèrent les mêmes résultats que l'année précédente : 5e en Championnat 
et Quart de finaliste de la Coupe d'Europe2. La saison 2006-2007, le club termine premier du Championnat au goal-average général. Mais le règlement va couronner La Roche-sur-Yon au goal-average... particulier entre les deux équipes (victoire de La Roche 8-4 contre une victoire 6-3 pour les Ploufraganais). Après plusieurs années où le club a enchainé de bons et de moins bons résultats, la saison 2015-2016 nous permet de participer à une Coupe d'Europe pour la saison 2016-2017 </P>
